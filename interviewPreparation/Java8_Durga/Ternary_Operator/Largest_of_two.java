package Ternary_Operator;

//Program to find largest of two no using Ternary Operator.






public class Largest_of_two {
	public static void main(String[] args) {
		

	int n1=10, n2 = 20;
	
	
	System.out.println("numbers "+n1+"  "+n2);
	
	int largest = (n1>n2)? n1:n2; // n1 is greater than n2 then after ? mark value means n1 excuted if it false after : colon n2 will be excuted
	
	System.out.println("Largest number is : "+largest);
	
	
	}

}
